#include "libft.h"
#include <string.h>
#include <stdio.h>

char		*ft_strcat(char *s1, const char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0')
		i++;
	if (*s2 != '\0')
	{
		while (*s2 != '\0')
		{
			s1[i] = *s2;
			s2++;
			i++;
		}
	}
	else
		s1[i] = '\0';
	return (s1);
}
