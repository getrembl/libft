#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	int c;
	int d;

	c = 0;
	d = 0;
	while ((n != 0) && (*s1 != '\0') && (*s2 != '\0'))
	{
		c = c + (*s1 - '0');
		c++;
		d = d + (*s2 - '0');
		d++;
		n--;
	}
	if (c > d)
		return (1);
	else if (c < d)
		return (-1);
	else
		return (0);
}
