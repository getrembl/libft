#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int c;
	int d;

	c = 0;
	d = 0;
	if (*s1 == '\0')
		d = *s2;
	if (*s2 == '\0')
		c = *s1;
	while ((*s1 != '\0' && *s2 != '\0'))
	{
		c = c + *s1;
		s1++;
		d = d + *s2;
		s2++;
		if (c != d)
			return (c - d);
	}
	return (c - d);
}
