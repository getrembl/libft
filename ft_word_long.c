#include "libft.h"

int		ft_word_long(char *s, char c, int i)
{
	int len;

	len = 0;
	while (s[i] != c)
	{
		len++;
		i++;
	}
	if (s[i] == '\0')
		return (-1);
	else
		return (len);
}
