#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int i;
	int d;
	const char *r1;
	const char *r2;

	r1 = s1;
	r2 = s2;
	while(*r1 == *r2 && n != 0)
	{
		r1++;
		r2++;
		n--;
	}
	if (*r1 == '\0' && *r2 == '\0')
		return (0);
	while ((*r1 != '\0' && n != 0) || (*r2 == '\0' && n != 0))
	{
		i = *r1 + '0';
		d = *r2 + '0';
		r1++;
		r2++;
	}
	return (i - d);
}
